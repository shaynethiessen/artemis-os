#!/bin/bash

echo "Simple and scientific calculator"
apt-get install --no-install-recommends -y kcalc

echo "KDE tool for mounting/formatting disks"
apt-get install --no-install-recommends -y partitionmanager

echo "Support for ntfs formatted drives"
apt-get install --no-install-recommends -y ntfs-3g

echo "Screenshot tool"
apt-get install --no-install-recommends -y kde-spectacle

echo "Archive Tool"
apt-get install --no-install-recommends -y ark

echo "7z and 7za file archivers with high compression ratio"
apt-get install --no-install-recommends -y p7zip-full

echo "Unarchiver for a variety of file formats"
apt-get install --no-install-recommends -y unar

echo "Unarchiver for .rar files"
apt-get install --no-install-recommends -y unrar-free

echo "Archiver for .rar files"
apt-get install --no-install-recommends -y rar

echo "Retrieves files from the web"
apt-get install --no-install-recommends -y wget

echo "Graphics file format thumbnailers for KDE SC"
apt-get install --no-install-recommends -y kdegraphics-thumbnailers

echo "KDE plugin to show thumbnails of Debian package files"
apt-get install --no-install-recommends -y kde-thumbnailer-deb

echo "Fast and lightweight video thumbnailer"
apt-get install --no-install-recommends -y ffmpegthumbnailer

echo "Quake-style terminal emulator based on KDE Konsole technology"
apt-get install --no-install-recommends -y yakuake